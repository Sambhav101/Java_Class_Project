    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        double onePizzaTotal = oneTotal();
        double tip = (double)tipBox.getValue();
        if (tip < 0) {
            showMessageDialog(null, "Tip can't be less than 0"); 
        } else if ((int)quantitySpinner.getValue() == 0) {
            showMessageDialog(null, "Quantity can't be 0");
        }
        else {
            totalBox.setValue(onePizzaTotal * ((int)quantitySpinner.getValue()) + tip);    
        }
    }//GEN-LAST:event_updateButtonActionPerformed

