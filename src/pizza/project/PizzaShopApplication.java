    private double oneTotal() {
        double total = 0;
        if (radioThin.isSelected()) {
            total += 17;
        } else if (radioThick.isSelected()) {
            total += 19.30;
        } else if (radioCheese.isSelected()) {
            total += 20.15;
        }
        
        if (checkPepperoni.isSelected()) {
            total += 2;
        }
        
        if (checkPineappple.isSelected()) {
            total += 2.4;
        }
        if (checkMushroom.isSelected()) {
            total += 3.4;
        }
        if (checkMeatball.isSelected()) {
            total += 2.10;
        }
        if (checkChicken.isSelected()) {
            total += 3.25;
        }
        if (checkOnions.isSelected()) {
            total += 1.8;
        }
        
        return total;
        
    }
    
    
    private boolean isCrustEmpty() {
        if (radioThin.isSelected() == false & radioThick.isSelected()== false & radioCheese.isSelected() == false) {
            return true;
        } else {
            return false;
        }
    }
    
