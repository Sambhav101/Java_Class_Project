    private void orderbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderbuttonActionPerformed
        // TODO add your handling code here:
        if (isCrustEmpty()) {
            showMessageDialog(null, "Please select at least one crust");
        } else if ((int)quantitySpinner.getValue() == 0) {
            showMessageDialog(null, "Quantity can't be 0");    
        } else {
            showMessageDialog(null, "Your Order is Complete. Thank you"); 
            clearCheckboxes();
            buttonGroup1.clearSelection();
            buttonGroup2.clearSelection();
            quantitySpinner.setValue((int)1);
            tipBox.setValue((int)0);
            tipCombobox.setSelectedIndex(0);
            
        }
        
    }//GEN-LAST:event_orderbuttonActionPerformed

    private void tipComboboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tipComboboxActionPerformed
        // TODO add your handling code here:
        double onePizzaTotal = oneTotal();
        if (tipCombobox.getSelectedIndex() == 1) {
            tipBox.setValue((double)(0.15 * (double)totalBox.getValue()));
        } else if (tipCombobox.getSelectedIndex() == 2) {
            tipBox.setValue((double)(0.2 * (double)totalBox.getValue()));
        } else if (tipCombobox.getSelectedIndex() == 3) {
            tipBox.setValue((double)(0.3 * (double)totalBox.getValue()));
        } else if (tipCombobox.getSelectedIndex() == 4) {
            tipBox.setValue((double)(0.4 * (double)totalBox.getValue()));
        } else {
            tipBox.setValue((double)0);
        }
        totalBox.setValue(onePizzaTotal * ((int)quantitySpinner.getValue()) + (double)tipBox.getValue());
    }//GEN-LAST:event_tipComboboxActionPerformed

